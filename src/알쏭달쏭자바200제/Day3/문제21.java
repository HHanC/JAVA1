package 알쏭달쏭자바200제.Day3;

public class 문제21 {
    public static void main(String[] args) {
        //초기화
        String s1 = "ABC";
        String s2 = "XYZ";

        System.out.println("문자열 s1은"+s1+"입니다");
        System.out.println("문자열 s2은"+s2+"입니다");

        s1 = "FBI"; //대입(값 변경)
        System.out.println("문자열 s1은"+s1+"입니다");
        System.out.println("문자열 s2은"+s2+"입니다");

    }
}
