package 알쏭달쏭자바200제.Day5;

public class 실전문1 {
    public static void main(String[] args) {
        int a = 1, b=3, c=5;

        System.out.println("a<b :"+(a<b));
        System.out.println("a<=b :"+(a<=b));
        System.out.println("b>c :"+(b>c));
        System.out.println("b>=c :"+(b>=c));
        System.out.println("a==b :"+(a==b));
        System.out.println("a!=b :"+(a!=b));
        System.out.println("a-b-c :"+(a-b-c));
        System.out.println("c-b-a :"+(c-b-a));
        System.out.println("a=b=c :"+(a=b=c));
        System.out.println("c=b=a :"+(c=b=a));

    }
}
