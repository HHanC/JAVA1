package 알쏭달쏭자바200제.Day1; // 프로젝트 명

public class 문제6 { // 클래스 명
    public static void main(String[] args) {
        // public : 모든 곳에서 접근이 가능하다.
        // static : 메모리에 한 번 할당되어 프로그램이 종료될 때 헤제된다.
        // void : 리턴값이 없다.
        // String[] args : 문자열을 배열로 사용한다.

        //방법1
        System.out.println(82+17); // 줄 바꿈 숫자 출력
        System.out.println(82-17); // 줄 바꿈 숫자 출력

        //방법2
        System.out.println("82+17="+(82+17)); // 줄 바꿈 문자 + 숫자 출력
        System.out.println("82-17="+(82-17)); // 줄 바꿈 문자 + 숫자 출력
    }
}
